        <h1>Exploring Nepal</h1>
        <p>Nepal isn't an easy country to visit and it's not cheap, but even with kids it's do-able. There is magic in the air, along with pollution. There are wonders to behold and tragedies both personal and environmental unfolding right in front of you. Nepal is special, another world sheltered by the majestic Himalayas and populated by some of the best and most beautiful people on the planet.</p>
        <p>We experiences major four season in Nepal throughout the year. They are summer (JUNE - AUGUST), Winter (DECEMBER - FEBRUARY), spring (MARCH TO MAY), and autumn (SEPTEMBER - MID DECEMBER). Choosing a best season for your holiday offers you more glimpse and pleasure. The best season for Annapurna base camp trekking is during spring (March - April) and autumn (October - December). Spring is offers the blooming Rhododendron as the trail is more attractive where all the trails turns into red pink and white flowers. However in autumn the skies are clear and snowcapped mountain views are amazing.</p>
        <p>Annapurna base Camp trekking is a moderate walk through the foothill of Annapurna Massif. It is not technical climb, you do not need to have previous experience even any altitude experiences to complete the trek. However, you have to physically fit and excellent strength, conditioning and endurance to be able to reach the base camp. So, we recommended to do some walking, cycling and other physical exercise before you coming Abc trek. Normally you must capable for 5/6 hours walking each days during the trek. With the physical fitness, mentally prepare is also important for outdoors. Because there are no star levels hotels for your comfortable. You have to adjust local tea houses with sharing Bathrooms and eat the foods which are available in tea houses menu. So you do need to be prepared yourself for these things.</p>
