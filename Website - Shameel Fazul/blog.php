        <p>As we already told you Annapurna base camp is a moderate trekking in Annapurna massif. However, to make a successful trek you should have proper trekking equipment which are most important. Among trekking gears proper hiking boots, a good down Jackets, trousers are most significant. Other gears besides are warm clothes, hiking poles, sun hat, Sunglass, gloves, comfortable trousers, warm sleeping bag are also important to have a successful and pleasant trekking to ABC</p>
        <p>While trekking above 3000 meter altitude, some people might suffer from altitude sickness. Normally altitude sickness occurs when your body do not adjust in high elevation where the oxygen level is very low. Symptoms of altitude sickness includes headache, vomiting, loss of sleep etc. If you get such Symptoms then you have immediately consult with your guide. Guides are well trained and experiences for altitude trekking and sickness problems. How to avoid Altitude sickness? Drink lots of waters as you can, eat your foods full each time, do not drink alcoholic beverages, can avoid the chances of sickness in Himalayas. Even though, if you feel such sickness you can use medicine like Diamox which can avoid sickness and help to go high elevation. The nest important things is when you are going up don't skip acclimatization because slowly and proper acclimatization adjust your body in high altitude.</p>
